// @ts-check
// @ts-nocheck
// Modifier Generator
// Reads dump_mod_names.json, creates missing modifier files in the correct
// subdirectory of wrapper/Objects/Modifiers/, then updates Imports.ts.
//
// Folder rules:
//   modifier_item_*   → Items/
//   modifier_rune_*   → Runes/
//   modifier_fountain_* | modifier_tower_* | modifier_watcher_* → Buildings/
//   modifier_{known_hero_prefix}_* → Abilities/{HeroFolder}/
//   everything else   → Base/

const fs = require('fs')
const path = require('path')

const root = path.resolve(__dirname, '..')
const sourceDir = path.join(root, 'wrapper', 'Objects', 'Modifiers')
const targetFile = path.join(root, 'wrapper', 'Imports.ts')
const namesFile = path.join(__dirname, 'dumps/dump_mod_names.json')

if (!fs.existsSync(sourceDir)) {
  console.error(`[!] Modifiers directory not found: ${sourceDir}`)
  process.exit(1)
}
if (!fs.existsSync(namesFile)) {
  console.error(`[!] Names file not found: ${namesFile}`)
  process.exit(1)
}

function getFiles(dir) {
  const files = []
  fs.readdirSync(dir).forEach((entry) => {
    const full = path.join(dir, entry)
    if (fs.statSync(full).isDirectory()) files.push(...getFiles(full))
    else files.push(full)
  })
  return files
}

function buildPrefixMap() {
  const map = new Map()
  const abilitiesDir = path.join(sourceDir, 'Abilities')
  if (!fs.existsSync(abilitiesDir)) return map
  fs.readdirSync(abilitiesDir).forEach((heroFolder) => {
    const heroPath = path.join(abilitiesDir, heroFolder)
    if (!fs.statSync(heroPath).isDirectory()) return
    fs.readdirSync(heroPath).forEach((f) => {
      const name = path.basename(f, '.ts')
      const segments = name.split('_').slice(1)
      for (let i = 1; i < segments.length; i++) {
        const prefix = segments.slice(0, i).join('_')
        if (!map.has(prefix)) map.set(prefix, heroFolder)
      }
    })
  })
  return map
}

function resolveFolder(modName, prefixMap) {
  const inner = modName.replace(/^modifier_/, '') // strip leading "modifier_"

  if (inner.startsWith('item_')) return path.join(sourceDir, 'Items')
  if (inner.startsWith('rune_')) return path.join(sourceDir, 'Runes')
  if (
    inner.startsWith('fountain_') ||
    inner.startsWith('tower_') ||
    inner.startsWith('watcher_')
  )
    return path.join(sourceDir, 'Buildings')

  // Try longest prefix match against known hero prefixes
  const segments = inner.split('_')
  for (let i = segments.length - 1; i >= 1; i--) {
    const prefix = segments.slice(0, i).join('_')
    if (prefixMap.has(prefix)) {
      return path.join(sourceDir, 'Abilities', prefixMap.get(prefix))
    }
  }

  return path.join(sourceDir, 'Base')
}

const wrapperDir = path.resolve(sourceDir, '..', '..')
// wrapper/Objects/  ← Base/Modifier lives here
const objectsDir = path.resolve(sourceDir, '..')

function generateFile(modName, targetFolder, deprecated = false) {
  const toWrapper = path.relative(targetFolder, wrapperDir).split(path.sep).join('/')
  const toObjects = path.relative(targetFolder, objectsDir).split(path.sep).join('/')
  const lines = [
    `import { WrapperClassModifier } from "${toWrapper}/Decorators"`,
    `import { Modifier } from "${toObjects}/Base/Modifier"`,
    '',
  ]
  if (deprecated) {
    lines.push(`/** @deprecated This modifier is no longer present in the game */`)
  }
  lines.push(
    `@WrapperClassModifier()`,
    `export class ${modName} extends Modifier {}`,
    ''
  )
  return lines.join('\n')
}

function isAutoGenerated(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf8')
    return (
      content.includes('@WrapperClassModifier()') &&
      /export class \w+ extends Modifier \{\}/.test(content) &&
      content.split('\n').filter((l) => l.trim()).length <= 6
    )
  } catch {
    return false
  }
}

const allMods = JSON.parse(fs.readFileSync(namesFile, 'utf8')).map(([name]) => name)
const prefixMap = buildPrefixMap()

let created = 0
let skipped = 0

const existingFileMap = new Map(
  getFiles(sourceDir).map((f) => [path.basename(f, '.ts'), f])
)
const knownMods = new Set(allMods)

// ── 1. Mark obsolete auto-generated stubs as @deprecated ──────────────────
let deprecated = 0
for (const [modName, filePath] of existingFileMap) {
  if (!knownMods.has(modName) && isAutoGenerated(filePath)) {
    const folder = path.dirname(filePath)
    fs.writeFileSync(filePath, generateFile(modName, folder, true))
    deprecated++
  }
}
if (deprecated > 0) console.log(`[!] Marked ${deprecated} obsolete modifiers as @deprecated`)

for (const modName of allMods) {
  const existingPath = existingFileMap.get(modName)
  if (existingPath !== undefined && !isAutoGenerated(existingPath)) {
    skipped++
    continue
  }
  const folder = resolveFolder(modName, prefixMap)
  fs.mkdirSync(folder, { recursive: true })
  const filePath = path.join(folder, `${modName}.ts`)
  fs.writeFileSync(filePath, generateFile(modName, folder))
  existingFileMap.set(modName, filePath)
  created++
}

console.log(`[+] Created/fixed ${created} modifier files`)
console.log(`[=] Skipped ${skipped} hand-crafted files`)

const allFiles = getFiles(sourceDir)
const validExportSet = new Set(
  allFiles.map((file) => {
    const fileName = path.basename(file, path.extname(file))
    const dirName = path
      .relative(sourceDir, path.dirname(file))
      .replace(/\\/g, '/')
    return `export { ${fileName} } from "./Objects/Modifiers/${dirName}/${fileName}"`
  })
)

const importsContent = fs.readFileSync(targetFile, 'utf8')
const lines = importsContent.split('\n')

const modifierExportRe = /^export \{ (\w+) \} from "\.\/Objects\/Modifiers\//
const filteredLines = lines.filter((line) => {
  const trimmed = line.trim()
  if (!modifierExportRe.test(trimmed)) return true // keep non-modifier lines
  return validExportSet.has(trimmed)
})

const existingSet = new Set(filteredLines.map((l) => l.trim()))
const newExports = [...validExportSet].filter((l) => !existingSet.has(l))

const removedExports = lines.length - filteredLines.length
if (removedExports > 0)
  console.log(`[-] Removed ${removedExports} obsolete exports from Imports.ts`)

if (newExports.length > 0) {
  const updated = filteredLines.join('\n') + '\n' + newExports.join('\n') + '\n'
  fs.writeFileSync(targetFile, updated)
  console.log(
    `[+] Added ${newExports.length} new exports to Imports.ts`,
    `\n[=] Total modifier exports: ${validExportSet.size}`
  )
} else if (removedExports > 0) {
  fs.writeFileSync(targetFile, filteredLines.join('\n'))
  console.log(`[=] Total modifier exports: ${validExportSet.size}`)
} else {
  console.log(
    `[=] No changes to Imports.ts`,
    `\n[=] Total modifier exports: ${validExportSet.size}`
  )
}
